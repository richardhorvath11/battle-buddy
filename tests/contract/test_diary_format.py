@pytest.mark.parametrize("case", CASES, ids=[case["id"] for case in CASES])
def test_matrix_case_resolves_exactly_as_expected(case):
    entries = _entries(case["entries_fixture"])
    resolution = resolve_format(case["template"], entries)
    expected = case["expected"]

    assert resolution["source"] == expected["source"], (
        "%s: source must be %r — %s; got %r"
        % (case["id"], expected["source"], case["why"], resolution["source"])
    )
    # Notices compared as a SET and exactly — never "contains" — so a spurious extra
    # notice (or a missing one) fails here.
