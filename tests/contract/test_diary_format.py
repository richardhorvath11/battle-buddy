    assert actual_notice_kinds == set(expected["notices"]), (
        "%s: notice kind set must equal %r exactly — %s; got %r"
        % (case["id"], sorted(expected["notices"]), case["why"], sorted(actual_notice_kinds))
    )
    assert resolution["structure"] == expected["resolved"], (
        "%s: resolved structure must equal the fixture's expectation field-for-field — "
        "%s" % (case["id"], case["why"])
    )


