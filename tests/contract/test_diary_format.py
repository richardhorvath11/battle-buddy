# ---------------------------------------------------------------------------
# Label pass-through (FR-005) — every block of entries-labeled-causal.json's
# sections is byte-identical in apply_format's output, plus the
# no-content-dropped property for its unmatched-label ("Timeline") block.
# ---------------------------------------------------------------------------


def _recovered_blocks(output):
    """Splits apply_format's output back into (heading_line, block) pairs.

    apply_format emits each piece as ``heading_line + "\\n" + block + "\\n\\n"``
    (data-model.md §8); none of the blocks in entries-labeled-causal.json's own
    `sections` list contain a blank line, so splitting the joined output on "\\n\\n"
    recovers each piece verbatim and a plain `==` comparison — never a substring `in`
    check a mutated block could still pass — is enough to prove byte-identity.
    """
    pieces = [piece for piece in output.split("\n\n") if piece]
    return [piece.split("\n", 1) for piece in pieces]


def test_label_pass_through_is_byte_identical_for_every_block():
    fixture = load_fixture("diary", "entries-labeled-causal.json")
    structure = resolve_format(None, fixture["entries"])["structure"]
    output = apply_format(structure, fixture["sections"])

    recovered = _recovered_blocks(output)
    recovered_blocks = [block for _heading, block in recovered]

    assert len(recovered) == len(fixture["sections"]), (
        "no content is ever dropped (data-model.md §8) — every input section must "
        "produce exactly one output piece"
    )
    for label, block in fixture["sections"]:
        assert block in recovered_blocks, (
            "label %r's block must survive apply_format byte-identical; got pieces %r"
            % (label, recovered)
        )


def test_field_order_label_with_no_matching_section_is_omitted():
    # The OMIT half of data-model.md §8: pair entries-labeled-causal.json's `sections`
    # against entries-consistent.json's resolved structure. That structure's
    # field_order carries "resolution" and "follow-ups", neither of which has a
    # matching block among entries-labeled-causal's sections — both must be omitted
    # entirely, never emitted as an empty heading.
    consistent_structure = resolve_format(None, _entries("entries-consistent"))["structure"]
    labeled_causal_sections = load_fixture("diary", "entries-labeled-causal.json")["sections"]

    output = apply_format(consistent_structure, labeled_causal_sections)

    assert "## Resolution" not in output
    assert "## Follow-ups" not in output
    # And the omission is not a side effect of dropping everything: the two sections
    # that DO match (What happened, Timeline) still emit, and the three unmatched
    # labeled-causal sections still get appended — no content silently vanishes.
    assert "## What happened" in output
    assert "## Timeline" in output
    assert "## Root cause (proposal)" in output
    assert "## Contributing factors (proposals)" in output
    assert "## Action items (proposals)" in output


# ---------------------------------------------------------------------------
# Bounded extraction — a large filler block with no heading, no line-initial
# Label:, and no date costs the extracted Structure nothing.
# ---------------------------------------------------------------------------


def test_extraction_is_bounded_by_a_large_content_free_filler_block():
    entries = _entries("entries-consistent")
    golden = GOLDEN["entries-consistent"][0]

    # No "#"/"**" heading line, no line-initial "Label:" field, and no date anywhere —
    # every sentence starts mid-word-safe lowercase prose with no colon and no digit.
    filler_sentence = (
        "this filler line carries no heading marker no inline label and no date "
    )
    filler = "\n".join([filler_sentence] * 5000)
    assert ":" not in filler and not any(character.isdigit() for character in filler)

    extended_content = entries[0]["content"] + "\n" + filler
    assert extract_structure(extended_content) == golden, (
        "a large, structure-free filler block must never change the extracted "
        "Structure — extraction reads only for title/headings/date/field_order "
        "(data-model.md 'Extraction is bounded')"
    )


# ---------------------------------------------------------------------------
# FR-003 input-signature property — format resolution's declared input
# vocabulary is disjoint from the session row's field names. Parsed
# DYNAMICALLY out of skills/session-store/references/schema.md's column
# table, never a test-local copy, per data-model.md §9's own instructions.
#
# Accepted-weak gate (data-model.md §9): the two vocabularies are disjoint
# by ordinary English, not by construction, so no plausible edit makes this
# fire. Its real value is the non-vacuity half below — proving the row-field
# parser actually works and the declared input set matches the prose.
# ---------------------------------------------------------------------------

_ROW_FIELD_RE = re.compile(r"^\| `([a-zA-Z_]+)` \|", re.MULTILINE)


def _parse_row_field_names():
    schema_path = REPO_ROOT / "skills" / "session-store" / "references" / "schema.md"
    text = schema_path.read_text(encoding="utf-8")
    start = text.index("## Column table")
    end = text.index("\n## ", start + 1)
    return frozenset(_ROW_FIELD_RE.findall(text[start:end]))


ROW_FIELD_NAMES = _parse_row_field_names()


def test_row_field_names_parsed_from_schema_are_non_vacuous():
    # The load-bearing half of this accepted-weak gate: prove the dynamic parser found
    # something real, not an empty set that would make the disjointness check below
    # vacuously true.
    assert ROW_FIELD_NAMES, "the schema.md column-table parser must find at least one field"
    assert "diary_url" in ROW_FIELD_NAMES, (
        "the parser must find diary_url specifically — its absence would mean the "
        "parser is reading the wrong table or the wrong file"
    )


def test_format_input_keys_disjoint_from_session_row_field_names():
    assert FORMAT_INPUT_KEYS.isdisjoint(ROW_FIELD_NAMES), (
        "FORMAT_INPUT_KEYS must share no name with a session-row column (data-model.md "
        "§9's FR-003 instrument)"
    )


def test_resolve_format_signature_disjoint_from_session_row_field_names():
    signature_params = set(inspect.signature(resolve_format).parameters)
    assert signature_params.isdisjoint(ROW_FIELD_NAMES), (
        "resolve_format's own parameter names must share no name with a session-row "
        "column either — the signature is the second half of the FR-003 instrument"
    )
