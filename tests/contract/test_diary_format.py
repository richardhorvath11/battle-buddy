# FR-003 input-signature property — format resolution's declared input
# vocabulary is disjoint from the session row's field names. Parsed
# DYNAMICALLY out of skills/session-store/references/schema.md's column
# table, never a test-local copy, per data-model.md §9's own instructions.
#
# Accepted-weak gate (data-model.md §9): the two vocabularies are disjoint
# by ordinary English, not by construction, so no plausible edit makes this
# fire. Its real value is the non-vacuity half below — proving the row-field
# parser actually works and the declared input set matches the prose.
# ---------------------------------------------------------------------------

_ROW_FIELD_RE = re.compile(r"^\| `([a-zA-Z_]+)` \|", re.MULTILINE)


def _parse_row_field_names():
    schema_path = REPO_ROOT / "skills" / "session-store" / "references" / "schema.md"
    text = schema_path.read_text(encoding="utf-8")
    start = text.index("## Column table")
    end = text.index("\n## ", start + 1)
    return frozenset(_ROW_FIELD_RE.findall(text[start:end]))


ROW_FIELD_NAMES = _parse_row_field_names()


def test_row_field_names_parsed_from_schema_are_non_vacuous():
    # The load-bearing half of this accepted-weak gate: prove the dynamic parser found
    # something real, not an empty set that would make the disjointness check below
    # vacuously true.
    assert ROW_FIELD_NAMES, "the schema.md column-table parser must find at least one field"
    assert "diary_url" in ROW_FIELD_NAMES, (
        "the parser must find diary_url specifically — its absence would mean the "
        "parser is reading the wrong table or the wrong file"
    )


def test_format_input_keys_disjoint_from_session_row_field_names():
    assert FORMAT_INPUT_KEYS.isdisjoint(ROW_FIELD_NAMES), (
        "FORMAT_INPUT_KEYS must share no name with a session-row column (data-model.md "
        "§9's FR-003 instrument)"
    )


def test_resolve_format_signature_disjoint_from_session_row_field_names():
    signature_params = set(inspect.signature(resolve_format).parameters)
    assert signature_params.isdisjoint(ROW_FIELD_NAMES), (
        "resolve_format's own parameter names must share no name with a session-row "
        "column either — the signature is the second half of the FR-003 instrument"
    )
