    entries = _entries("entries-inconsistent")
    resolution = resolve_format(None, entries)

    assert set(notice["kind"] for notice in resolution["notices"]) == {"entries_inconsistent"}
    # The newest entry (index 0 — read_recent is most-recent-first) is what freshest-wins
