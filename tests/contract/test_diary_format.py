# The template branch's no-read property (US1 AS-1) — a real trap, not a
# plain list, so any consumption at all (iteration, len, truth-test,
# indexing) fails loudly rather than silently.
# ---------------------------------------------------------------------------


class _RaisingEntries:
    """Stands in for ``entries`` on the template-configured path. Every way a
    caller might read it — iterate, measure, truth-test, index — raises, so
    a ``resolve_format`` that touches ``entries`` at all on this path fails
    the test instead of merely "happening" to work because a real list was
    never actually consumed."""

    def __iter__(self):
        raise AssertionError("entries must not be iterated when a template is configured")

    def __len__(self):
        raise AssertionError("entries must not be measured when a template is configured")

    def __bool__(self):
        raise AssertionError("entries must not be truth-tested when a template is configured")

    def __getitem__(self, item):
        raise AssertionError("entries must not be indexed when a template is configured")


def test_template_branch_never_reads_entries():
    resolution = resolve_format("a well-formed configured template", _RaisingEntries())
    assert resolution == {
        "source": "template",
        "structure": None,
        "template": "a well-formed configured template",
        "notices": [],
    }


